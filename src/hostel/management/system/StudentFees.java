    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String id = (String) studentid.getSelectedItem();
        String sname = name.getText();
        String room = roomnumber.getText();
        String Month = month.getText();
        String price = roomprice.getText();
        String spay = pay.getText();
        String date = paydate.getText();
        
        
      
      
       try{
            Connection con = ConnectionProvider.getCon();
            PreparedStatement stmt = con.prepareStatement("INSERT INTO STUDENTPAYMENT VALUES (?, ?, ?, ?, ?, ?, ?)");
            stmt.setString(1, id);
            
            stmt.setString(2, sname);
            
            stmt.setString(3, room);
            
            stmt.setString(4,price);
            
            stmt.setString(5, Month);
            
            stmt.setString(6, spay);
            
            stmt.setString(7, date);
            

            int rowsInserted = stmt.executeUpdate();
            if(rowsInserted > 0)
            {
                JOptionPane.showMessageDialog(this,  " Payment Successfull");
            }
            else{
                JOptionPane.showMessageDialog(this, "Failed");
            }
            con.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
         
    }//GEN-LAST:event_saveActionPerformed

