        private void StudentId(){
      try {
        // Establish the database connection
        Connection con = ConnectionProvider.getCon();
        
        // Prepare the SQL query to fetch ADHAR_NO where status is 'Living'
        PreparedStatement stmt = con.prepareStatement("SELECT * FROM ADDSTUDENT WHERE status = 'Living'");
        
        // Execute the query
        ResultSet rs = stmt.executeQuery();
        
        // Iterate through the result set and add ADHAR_NO to the combo box
        while (rs.next()) {
            studentid.addItem(rs.getString("ADHAR_NO"));
        }
        
    } catch (SQLException e) {
        // Handle SQL exception
        System.err.println("Error fetching student IDs: " + e.getMessage());
        JOptionPane.showMessageDialog(this, "Database error.");
    }
}
